package POJO.IPLocate;

public class Company {
    private String name;
    private String domain;
    private String country_code;
    private String type;

    public String getName() {
        return name;
    }

    public String getDomain() {
        return domain;
    }

    public String getCountry_code() {
        return country_code;
    }

    public String getType() {
        return type;
    }

}
